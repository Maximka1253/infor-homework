# Красивый вывод

def run(title, sql):
    print(f"\n{'='*60}")
    print(f"  {title}")
    print('='*60)
    cur.execute(sql)
    rows = cur.fetchall()
    cols = [d[0] for d in cur.description]
    print("  " + " | ".join(f"{c:<30}" for c in cols))
    print("  " + "-" * (33 * len(cols)))
    for row in rows:
        print("  " + " | ".join(f"{str(v):<30}" for v in row))


#Количество всех студентов

run("1. Количество всех студентов","""
    SELECT COUNT(*) AS всего_студентов
    FROM студенты
""")

#Количество студентов по направлениям

run("2. Количество студентов по направлениям", """
    SELECT н.название AS направление,
           COUNT(*)   AS количество
    FROM студенты с
    JOIN направления н ON с.id_направления = н.id_направления
    GROUP BY н.название
    ORDER BY количество DESC
""")

#Количество студентов по формам обучения

run("3. Количество студентов по формам обучения", """
    SELECT т.название AS форма_обучения,
           COUNT(*)   AS количество
    FROM студенты с
    JOIN типы_обучения т ON с.id_типа_обучения = т.id_типа
    GROUP BY т.название
    ORDER BY количество DESC
""")

#Макс/мин/средний балл по направлениям

run("4. Макс/мин/средний балл по направлениям", """
    SELECT н.название        AS направление,
           MAX(средний_балл) AS максимум,
           MIN(средний_балл) AS минимум,
           ROUND(AVG(средний_балл), 2) AS среднее
    FROM студенты с
    JOIN направления н ON с.id_направления = н.id_направления
    GROUP BY н.название
    ORDER BY н.название
""")

#Средний балл по направлениям, уровням, формам

run("5. Средний балл по направлениям, уровням и формам обучения", """
    SELECT н.название AS направление,
           у.название AS уровень,
           т.название AS форма,
           ROUND(AVG(с.средний_балл), 2) AS средний_балл
    FROM студенты с
    JOIN направления    н ON с.id_направления   = н.id_направления
    JOIN уровень_обучения у ON с.id_уровня       = у.id_уровня
    JOIN типы_обучения  т ON с.id_типа_обучения  = т.id_типа
    GROUP BY н.название, у.название, т.название
    ORDER BY н.название, у.название, т.название
""")

#Топ-5 для повышенной стипендии

run("6. Топ-5 студентов «Прикладная Информатика» очной формы", """
    SELECT с.фамилия, с.имя, с.отчество, с.средний_балл
    FROM студенты с
    JOIN направления   н ON с.id_направления   = н.id_направления
    JOIN типы_обучения т ON с.id_типа_обучения  = т.id_типа
    WHERE н.название = 'Прикладная Информатика'
      AND т.название = 'Очная'
    ORDER BY с.средний_балл DESC
    LIMIT 5
""")

